function initI18n() {
    const savedLang = localStorage.getItem('opencommands_lang');
    const browserLang = navigator.language.split('-')[0];
    const supportedLangs = languages.map(l => l.code);

    // Exact match or primary language match
    if (savedLang && supportedLangs.includes(savedLang)) {
        currentLang = savedLang;
    } else {
        const matchedLang = languages.find(l => l.code === navigator.language || l.code.startsWith(browserLang));
        currentLang = matchedLang ? matchedLang.code : 'pt-BR';
    }

    document.documentElement.lang = currentLang;
    updateStaticTranslations();
    renderLanguageSelector();
}

